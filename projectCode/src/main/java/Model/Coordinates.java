package Model;

public class Coordinates {
    private int x;
    private int y;
    private int z;





    ////methods////
    public Coordinates(int x, int y, int z)
    {
        this.x = x;
        this.y = y;
        this.z = z;
    }



    //getters
    public int getX() {
        return x;
    }


    public int getY() {
        return y;
    }


    public int getZ() {
        return z;
    }
}
